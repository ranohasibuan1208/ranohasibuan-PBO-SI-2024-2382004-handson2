public class Unaryoperator {
    public static void main(String[] args) {
        int x = 10;
        // Pre-Increment
        System.out.println(++x);
        // Post-Increment
        System.out.println(x++);

        int y = 10;
        // Pre-Decrement
        System.out.println(--y);
        // Post-Decrement
        System.out.println(y--);

        // Logical NOT
        boolean isTrue = true;
        System.out.println(!isTrue);
    }
}
