package binaryoperator;

public class Aritmatika {
    public static void main(String[] args) {
        int operand1 = 60;
        int operand2 = 30;

        System.out.println(operand1 + operand2);
        System.out.println(operand1 - operand2);
        System.out.println(operand1 * operand2);
        System.out.println(operand1 / operand2);
        System.out.println(operand1 & operand2);

    }
}